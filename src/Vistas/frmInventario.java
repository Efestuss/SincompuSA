    //Método para cargar datos
    public void cargarDatos() throws ClassNotFoundException, SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableInventario.getModel();
        modelo.setRowCount(0); // Limpia los datos existentes en la tabla

        ctrlBodegueroInventario controlador = new ctrlBodegueroInventario();

        List<Inventario> inventario = controlador.obtenerInventario();

        for (Inventario inv : inventario) {
            String estado = calcularEstado(inv.getCantidad());
            modelo.addRow(new Object[]{inv.getIdProducto(), inv.getNombre(), inv.getCantidad(), inv.getPrecio(), inv.getCategoria(), inv.getProveedor(), estado});
        }
    }

    private String calcularEstado(int cantidad) {
        return (cantidad == 0) ? "AGOTADO" : "EN STOCK";
    }

    public void configurarRenderizadorEstado() {
        TableColumn estadoColumn = jTableInventario.getColumnModel().getColumn(6);
        estadoColumn.setCellRenderer(new frmInventario.CustomComboBoxRenderer());
    }

    // Renderizador de JComboBox personalizado
    class CustomComboBoxRenderer extends JComboBox<String> implements TableCellRenderer {

        CustomComboBoxRenderer() {
            addItem("EN STOCK");
            addItem("AGOTADO");
            setEditable(false); // Establecer el JComboBox como no editable

        }

        @Override
        public java.awt.Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            int cantidad = Integer.parseInt(table.getValueAt(row, 2).toString()); // Obtener la cantidad

            if (cantidad == 0) {
                setSelectedItem("AGOTADO");
                setBackground(Color.RED); // Fondo rojo para "AGOTADO"
            } else {
                setSelectedItem("EN STOCK");
                setBackground(Color.GREEN); // Fondo verde para "EN STOCK"
            }

            return this;
        }
    }

