    public void cargarDatos() throws ClassNotFoundException {
        DefaultTableModel modelo = (DefaultTableModel) jTableProveedor.getModel();
        modelo.setRowCount(0); // Limpia los datos existentes en la tabla

        ctrlProveedor controlador = new ctrlProveedor();
        List<Proveedor> proveedores = controlador.obtenerProveedores();

        for (Proveedor proveedor : proveedores) {
            modelo.addRow(new Object[]{proveedor.getIdProveedor(), proveedor.getNombre(), proveedor.getDireccion(), proveedor.getTelefono(), proveedor.getEmail()});
        }
    }

    public void actualizarTabla() {

        // Actualizar y refrescar la tabla
        DefaultTableModel modeloTabla = (DefaultTableModel) jTableProveedor.getModel();
        modeloTabla.setRowCount(0); // Limpiar todos los datos actuales en la tabla

        // Volver a cargar los datos desde la base de datos
        ctrlProveedor proveedor = new ctrlProveedor();
        proveedor.cargarDatosTablaProveedor(modeloTabla);

    }

