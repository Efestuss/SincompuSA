    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // Fila seleccionada
        int filaSeleccionada = jTableProveedor.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un proveedor para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        frmProveedor frmProveedor = new frmProveedor();
        // Verificar si frmProveedor está inicializado
        if (frmProveedor == null) {
            // Si no está inicializado, muestra un mensaje de error o realiza alguna acción apropiada.
            JOptionPane.showMessageDialog(this, "frmProveedor no está inicializado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String idProveedor = jTableProveedor.getValueAt(filaSeleccionada, 0).toString();
        String nombre = jTableProveedor.getValueAt(filaSeleccionada, 1).toString();
        String direccion = jTableProveedor.getValueAt(filaSeleccionada, 2).toString();
        String telefono = jTableProveedor.getValueAt(filaSeleccionada, 3).toString();
        String email = jTableProveedor.getValueAt(filaSeleccionada, 4).toString();

        // Instanciar la clase que contiene el formulario deseado
        frmModificarProveedor modificarProveedor = new frmModificarProveedor(this, idProveedor, nombre, direccion, telefono, email);
        modificarProveedor.setVisible(true);

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int filaSeleccionada = jTableProveedor.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un proveedor para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este proveedor?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Obtén el ID del proveedor seleccionado en la tabla
            Object idProveedorObj = jTableProveedor.getValueAt(filaSeleccionada, 0);
            // Verifica si el ID del proveedor es nulo
            if (idProveedorObj == null) {
                JOptionPane.showMessageDialog(this, "El ID del proveedor seleccionado es nulo.", "Error", JOptionPane.ERROR_MESSAGE);
                System.out.println("El id es nulo");
                return;
            }
            String idProveedor = idProveedorObj.toString();
            System.out.println("El id no es nulo: " + idProveedor);
            // Llama al método para eliminar el proveedor
            ctrlProveedor proveedor = new ctrlProveedor();
            proveedor.eliminarTaller(idProveedor);
            actualizarTabla();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

